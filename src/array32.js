// ## Write a function `alphaSort` that sorts an array of strings alphabetically.

// - Examples:
//   - alphaSort(['b', 'a', 'c']) --> ['a', 'b', 'c']


function alphaSort(arr) {
    return arr.sort();
  }
  
  // Example usage:
  console.log(alphaSort(['b', 'a', 'c'])); // Output: ['a', 'b', 'c']
  console.log(alphaSort(['dog', 'cat', 'elephant', 'bird'])); // Output: ['bird', 'cat', 'dog', 'elephant']
  